# 실수인 경우
su = 99.999999
print("내가 좋아하는 숫자는",su, "이다")
print("내가 좋아하는 숫자는 %.2f 이다" %(su))
print("내가 좋아하는 숫자는 {0:.2f} 이다.".format(su))
